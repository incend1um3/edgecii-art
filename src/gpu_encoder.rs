use std::{io::Write, num::NonZero, path::Path};

use gpu_video::{
    BytesEncoderH264, InputFrame, RawFrameData, VideoAdapter, VideoDevice, VideoInstance,
    parameters::{
        EncoderOutputParameters, EncoderParametersH264, H265Profile, RateControl,
        VideoAdapterDescriptor, VideoDeviceDescriptor, VideoInstanceDescriptor, VideoParameters,
    },
};

pub struct GpuEncoder {
    encoder: BytesEncoderH264,
    file: std::fs::File,
    width: u32,
    height: u32,
    seq: u64,
}

impl GpuEncoder {
    pub fn new(
        width: NonZero<u32>,
        height: NonZero<u32>,
        fps: f32,
        output: &Path,
    ) -> anyhow::Result<Self> {
        let instance = VideoInstance::new(&VideoInstanceDescriptor {
            enable_api_dump: false,
            enable_validations: true,
        })?;
        instance
            .iter_adapters()?
            .for_each(|a| println!("{:#?}", a.info()));
        let adapter = instance
            .create_adapter(&VideoAdapterDescriptor {
                supports_decoding: false,
                supports_encoding: true,
            })
            .unwrap();
        let device = adapter
            .create_device(&VideoDeviceDescriptor::default())
            .unwrap();

        let (num, den) = f32_to_fraction(fps, 1000);
        let mut output_params =
            device.encoder_output_parameters_h264_high_quality(RateControl::Disabled)?;
        output_params.max_references = Some(NonZero::new(1).unwrap());
        let encoder = device.create_bytes_encoder_h264(EncoderParametersH264 {
            input_parameters: VideoParameters {
                width,
                height,
                target_framerate: gpu_video::parameters::Rational {
                    numerator: num,
                    denominator: NonZero::new(den).unwrap(),
                },
            },
            output_parameters: output_params,
        })?;

        Ok(Self {
            encoder,
            file: std::fs::File::create(output)?,
            width: width.get(),
            height: height.get(),
            seq: 0,
        })
    }

    pub fn encode_frame(&mut self, frame: ndarray::Array3<u8>) -> anyhow::Result<()> {
        let (frame, _) = frame
            .as_standard_layout()
            .to_owned()
            .into_raw_vec_and_offset();

        let yuv420 = rgb2yuv420::convert_rgb_to_yuv420sp_nv12(&frame, self.width, self.height, 3);

        let bytes = self.encoder.encode(
            &InputFrame {
                data: RawFrameData {
                    frame: yuv420,
                    width: self.width,
                    height: self.height,
                },
                pts: Some(self.seq),
            },
            false,
        )?;

        self.seq += 1;
        self.file.write_all(&bytes.data)?;

        Ok(())
    }
}

impl Drop for GpuEncoder {
    fn drop(&mut self) {
        self.file.flush().unwrap();
    }
}

