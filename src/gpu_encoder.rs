fn f32_to_fraction(value: f32, max_denominator: u32) -> (u32, u32) {
    assert!(
        value.is_finite() && value >= 0.0,
        "value must be non-negative and finite"
    );

    if value == 0.0 {
        return (0, 1);
    }

    let (mut h_prev, mut h_curr): (u64, u64) = (0, 1);
    let (mut k_prev, mut k_curr): (u64, u64) = (1, 0);

    let mut x = value as f64;

    loop {
        let a = x.floor();
        let a_u = a as u64;

        let h_next = a_u * h_curr + h_prev;
        let k_next = a_u * k_curr + k_prev;

        if k_next > max_denominator as u64 {
            break;
        }

        h_prev = h_curr;
        h_curr = h_next;
        k_prev = k_curr;
        k_curr = k_next;

        let frac = x - a;
        if frac < 1e-9 {
            break; // exact (within float precision)
        }
        x = 1.0 / frac;
    }

    (h_curr as u32, k_curr as u32)
}
